    def test_pressureConvert(self):
        self.assertAlmostEqual(Pressure.BarToPascal(0),0)
        self.assertAlmostEqual(Pressure.BarToPascal(1),a)
        self.assertAlmostEqual(Pressure.BarToPascal(100.5),100.5 * a)
        self.assertAlmostEqual(Pressure.AtmToPascal(0),0)
        self.assertAlmostEqual(Pressure.AtmToPascal(1),b)
        self.assertAlmostEqual(Pressure.AtmToPascal(100.5),100.5 * b)
        self.assertAlmostEqual(Pressure.TorrToPascal(0),0)
        self.assertAlmostEqual(Pressure.TorrToPascal(1),b/c)
        self.assertAlmostEqual(Pressure.TorrToPascal(100.5),100.5 * (b/c))
        self.assertAlmostEqual(Pressure.mmHgToPascal(0),0)
        self.assertAlmostEqual(Pressure.mmHgToPascal(1),d*e)
        self.assertAlmostEqual(Pressure.mmHgToPascal(100.5),100.5 * d*e)

        self.assertAlmostEqual(Pressure.PascalToBar(0),0)
        self.assertAlmostEqual(Pressure.PascalToBar(1),1/a)
        self.assertAlmostEqual(Pressure.PascalToBar(100.5),100.5 *(1/a) )
        self.assertAlmostEqual(Pressure.AtmToBar(0),0)
        self.assertAlmostEqual(Pressure.AtmToBar(1),b/a)
        self.assertAlmostEqual(Pressure.AtmToBar(100.5),100.5 * (b/a))
        self.assertAlmostEqual(Pressure.TorrToBar(0),0)
        self.assertAlmostEqual(Pressure.TorrToBar(1),b/(a*c))
        self.assertAlmostEqual(Pressure.TorrToBar(100.5),100.5 * (b/(a*c)))
        self.assertAlmostEqual(Pressure.mmHgToBar(0),0)
        self.assertAlmostEqual(Pressure.mmHgToBar(1),(d*e)/a)
        self.assertAlmostEqual(Pressure.mmHgToBar(100.5),100.5 * ((d*e)/a))

        self.assertAlmostEqual(Pressure.PascalToAtm(0),0)
        self.assertAlmostEqual(Pressure.PascalToAtm(1),1/b)
        self.assertAlmostEqual(Pressure.PascalToAtm(100.5),100.5 *(1/b) )
        self.assertAlmostEqual(Pressure.BarToAtm(0),0)
        self.assertAlmostEqual(Pressure.BarToAtm(1),a/b)
        self.assertAlmostEqual(Pressure.BarToAtm(100.5),100.5 * (a/b))
        self.assertAlmostEqual(Pressure.TorrToAtm(0),0)
        self.assertAlmostEqual(Pressure.TorrToAtm(1),1/c)
        self.assertAlmostEqual(Pressure.TorrToAtm(100.5),100.5 * (1/c))
        self.assertAlmostEqual(Pressure.mmHgToAtm(0),0)
        self.assertAlmostEqual(Pressure.mmHgToAtm(1),(d*e)/b)
        self.assertAlmostEqual(Pressure.mmHgToAtm(100.5),100.5 * ((d*e)/b))

        self.assertAlmostEqual(Pressure.PascalToTorr(0),0)
        self.assertAlmostEqual(Pressure.PascalToTorr(1),c/b)
        self.assertAlmostEqual(Pressure.PascalToTorr(100.5),100.5 *(c/b) )
        self.assertAlmostEqual(Pressure.BarToTorr(0),0)
        self.assertAlmostEqual(Pressure.BarToTorr(1),(a*c)/b)
        self.assertAlmostEqual(Pressure.BarToTorr(100.5),100.5 * ((a*c)/b))
        self.assertAlmostEqual(Pressure.AtmToTorr(0),0)
        self.assertAlmostEqual(Pressure.AtmToTorr(1),c)
        self.assertAlmostEqual(Pressure.AtmToTorr(100.5),100.5 * c)
        self.assertAlmostEqual(Pressure.mmHgToTorr(0),0)
        self.assertAlmostEqual(Pressure.mmHgToTorr(1),(c*d*e)/b)
        self.assertAlmostEqual(Pressure.mmHgToTorr(100.5),100.5 * ((c*d*e)/b))

        self.assertAlmostEqual(Pressure.PascalTommHg(0),0)
        self.assertAlmostEqual(Pressure.PascalTommHg(1),1/(d*e))
        self.assertAlmostEqual(Pressure.PascalTommHg(100.5),100.5 *(1/(d*e)) )
        self.assertAlmostEqual(Pressure.BarTommHg(0),0)
        self.assertAlmostEqual(Pressure.BarTommHg(1),a/(d*e))
        self.assertAlmostEqual(Pressure.BarTommHg(100.5),100.5 * (a/(d*e)))
        self.assertAlmostEqual(Pressure.AtmTommHg(0),0)
        self.assertAlmostEqual(Pressure.AtmTommHg(1),b/(e*d))
        self.assertAlmostEqual(Pressure.AtmTommHg(100.5),100.5 * (b/(d*e)))
        self.assertAlmostEqual(Pressure.TorrTommHg(0),0)
        self.assertAlmostEqual(Pressure.TorrTommHg(1),b/(c*d*e))
        self.assertAlmostEqual(Pressure.TorrTommHg(100.5),100.5 * (b/(c*d*e)))

        self.assertAlmostEqual(Pressure.PsiToPascal(0),0)
        self.assertAlmostEqual(Pressure.PsiToPascal(1),6894.8)
        self.assertAlmostEqual(Pressure.PsiToPascal(100.5),100.5 * 6894.8 )
        self.assertAlmostEqual(Pressure.PsiToBar(0),0)
        self.assertAlmostEqual(Pressure.PsiToBar(1),6.8948/100)
        self.assertAlmostEqual(Pressure.PsiToBar(100.5),100.5 * (6.8948/100))
        self.assertAlmostEqual(Pressure.PsiToAtm(0),0)
        self.assertAlmostEqual(Pressure.PsiToAtm(1),6.8046/100)
        self.assertAlmostEqual(Pressure.PsiToAtm(100.5),100.5 * (6.8046/100))
        self.assertAlmostEqual(Pressure.PsiToTorr(0),0)
        self.assertAlmostEqual(Pressure.PsiToTorr(1),51.71493)
        self.assertAlmostEqual(Pressure.PsiToTorr(100.5),100.5 * 51.71493)

        self.assertAlmostEqual(Pressure.PascalToPsi(0),0)
        self.assertAlmostEqual(Pressure.PascalToPsi(1),(1.450377/10000))
        self.assertAlmostEqual(Pressure.PascalToPsi(100.5),100.5 * (1.450377/10000))
        self.assertAlmostEqual(Pressure.BarToPsi(0),0)
        self.assertAlmostEqual(Pressure.BarToPsi(1),14.50377)
        self.assertAlmostEqual(Pressure.BarToPsi(100.5),100.5 * 14.50377)
        self.assertAlmostEqual(Pressure.AtmToPsi(0),0)
        self.assertAlmostEqual(Pressure.AtmToPsi(1),14.69595)
        self.assertAlmostEqual(Pressure.AtmToPsi(100.5),100.5 * 14.69595)
        self.assertAlmostEqual(Pressure.TorrToPsi(0),0)
        self.assertAlmostEqual(Pressure.TorrToPsi(1),(1.933678/100))
        self.assertAlmostEqual(Pressure.TorrToPsi(100.5),100.5 * (1.933678/100))

