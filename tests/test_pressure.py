    def test_pressure(self):
        self.assertAlmostEqual(Pressure.ForceAndArea(0,1),0)
        self.assertAlmostEqual(Pressure.ForceAndArea(2.5,10.7),2.5/10.7)

        self.assertAlmostEqual(Pressure.EnergyAndVolume(0,1),0)
        self.assertAlmostEqual(Pressure.EnergyAndVolume(2.5,10.7),2.5/10.7)

        self.assertAlmostEqual(Pressure.Gas(1,0,0,0),0)
        self.assertAlmostEqual(Pressure.Gas(2,2,2,2),(2*2*2)/2)

        self.assertAlmostEqual(Pressure.KineticMass(1,0,1),0)
        self.assertAlmostEqual(Pressure.KineticMass(1,1,1),0.5)

        self.assertAlmostEqual(Pressure.KineticRho(1,1),0.5)

        self.assertAlmostEqual(Pressure.PotentialMass(1,1,1),g)

        self.assertAlmostEqual(Pressure.PotentialRho(1,1),g)
    
